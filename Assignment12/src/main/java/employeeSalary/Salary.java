package employeeSalary;

public class Salary {
	int bp,ded,bonus;
	
	public void setBasicPay(int bp) {
		this.bp=bp;
	}
	public int getBasicPay() {
		return bp;
	}

	public int getDed() {
		return ded;
	}

	public void setDed(int ded) {
		this.ded = ded;
	}

	public int getBonus() {
		return bonus;
	}

	public void setBonus(int bonus) {
		this.bonus = bonus;
	}

}
